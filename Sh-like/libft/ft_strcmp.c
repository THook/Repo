#include <string.h>

int			ft_strcmp(const char *s1, const char *s2)
{
	int		i;

	if (s1 == NULL && s2 == NULL)
		return (0);
	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			return (s1[i] - s2[i]);
		}
		i++;
	}
	return (0);
}
