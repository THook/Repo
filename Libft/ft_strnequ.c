#include <string.h>

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!s1 || !s2 || n == 0)
		return (0);
	while ((*s1 && n > 1) && *s1 == *s2)
	{
		s1++;
		s2++;
		n--;
	}
	if ((*s1 == *s2))
		return (1);
	else
		return (0);
}
